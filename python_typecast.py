""" Casting in python is therefore done using constructor functions """
# int() - constructs an integer number from an integer literal, a float literal or a string literal
INT_VAR1 = int(0.555)
INT_VAR2 = int(2.8)
INT_VAR3 = int("3")

print(INT_VAR1)
print(INT_VAR2)
print(INT_VAR3)

# float() - constructs an float number from an integer literal, a float literal or a string literal
float_var1 = float(1)
float_var2 = float(134)
float_var3 = float("3")
float_var4 = float("4.2")

print(float_var1)
print(float_var2)
print(float_var3)
print(float_var4)

# str() - constructs a string from a wide variety of data types,
# including strings, integer literals and float literals
STR1 = str("s1")
STR2 = str(2)
STR3 = str(3.0)

print(STR1)
print(STR2)
print(STR3)
